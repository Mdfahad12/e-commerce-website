const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    minlength: 3,
    maxlength: 255,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    minlength: 5,
    maxlength: 255,
  },
  password: {
    type: String,
    required: true,
    minlength: 6, // Adjust the minimum length as needed
    maxlength: 1024, // Adjust the maximum length as needed
  },
});

const User = mongoose.model('User', userSchema);

module.exports = User;
